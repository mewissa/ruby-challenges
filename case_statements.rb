weather = 'overcast'

case(weather)
when 'sunny'
	puts "It's sunny.. Hopefully it's not too hot. Tank top and SUNSCREEN"
when 'rainy'
	puts "IT'S RAINING!!!!!! YAAAAAY!!!! Stay dry with a raincoat and cozy up!"
when 'overcast'
	puts "Not too hot, not too cold. Safe with a t-shirt, but carry the hoodie anyway."
when 'cold'
	puts "Chilly scarf weather! Yay!"
when 'foggy'
	puts "Is it morning fog? Because morning fog is the best. Use a light jacket so it's not cumbersome to carry later"
end